
public class Data {
	
	private String name, age, number, mailid;
	
	public Data() {
		super();
	}
	
	public Data(String name, String age, String number, String mailid) {
		super();
		this.name = name;
		this.age = age;
		this.number = number;
		this.mailid = mailid;
		
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getAge() {
		return age;
	}
	
	public void setAge(String age) {
		this.age = age;
	}
	
	public String getNumber() {
		return number;
	}
	
	public void setNumber(String number) {
		this.number = number;
	}
	
	public String getMailid() {
		return mailid;
	}
	
	public void setMailid(String mailid) {
		this.mailid = mailid;
	}
	
}
	
